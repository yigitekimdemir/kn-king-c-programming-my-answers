/* Write a program that declares several int and float variables - without
initializing them - and then prints their values. Is there any pattern to the values? (Usually there isn't.) */

#include <stdio.h>

int main(void)
{
    int a, b;
    float c, d;

    printf("%d\n", a);
    printf("%d\n", b);
    printf("%f\n", c);
    printf("%f\n", d);

    return 0;
}

// Printed values has no pattern or meaning.



